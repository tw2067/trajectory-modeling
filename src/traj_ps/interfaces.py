from typing import Protocol, Any
import pandas as pd

class TrajectoryModel(Protocol):
    name: str
    def fit(self, samples: list[dict], **kwargs) -> "TrajectoryModel": ...
    def embed(self, samples: list[dict]) -> pd.DataFrame: ...
    def ps(self, samples: list[dict]) -> pd.DataFrame: ...
    def save(self, path: str) -> None: ...
    @classmethod
    def load(cls, path: str) -> "TrajectoryModel": ...
